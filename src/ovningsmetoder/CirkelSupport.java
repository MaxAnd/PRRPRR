public class CirkelSupport {
    
    public double omkrets(double r){
        
        double om;
        om = (r*2)*Math.PI;
        return om;
        
    }//end omkrets
    
    public double area(double r){
        
        double a;
        a = (r*r)*Math.PI;
        return a;
        
    }//end area
    
}//main
